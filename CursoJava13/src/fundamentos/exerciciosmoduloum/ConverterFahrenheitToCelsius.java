package fundamentos.exerciciosmoduloum;

import java.util.Scanner;

public class ConverterFahrenheitToCelsius {
	
	public static void main(String[] args) {
		
		double f;
		double c;
		final int const1 = 32;
		final double const2 = 1.8;
		
		Scanner entrada = new Scanner(System.in);
		
			System.out.print("Digite a temperatura Fahrenheit: ");
			f = entrada.nextDouble();
			
			c = (f-const1)/const2;
			
			System.out.printf("A temperatura %.2f°F em Celsius é %.2f°",f,c);
			
		entrada.close();
		
	}

}
